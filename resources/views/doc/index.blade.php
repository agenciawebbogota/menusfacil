                    <h2 class="dotted" id="administrador"><i>Administrador</i></h2>

                    <p><span class="menus-facil">Menús Fácil</span> cuenta con un <strong>Módulo Administrador</strong> desde el cual se gestionan todas las funciones del aplicativo. Es accesible desde cualquier dispositivo con conexión a Internet.</p>

                    <h3 id="vista-administrador" style="color:#2a2730;"><strong>Vista Administrador</strong></h3>

                    <p>Ingresando con su usuario <i>(dirección de correo electrónico)</i>  y contraseña, accede a la vista de administrador.</p>

                    <img src="/doc/images/modulo-administrador-1.jpg" alt="">

                    <ol class="clear">
                        <li>Tabla de menús</li>
                        <li>Crear nuevo menú/adición.</li>
                        <li>Generar pdf menús.</li>
                        <li>Vista de pedidos en tiempo real.</li>
                        <li>Generar pdf pedidos.</li>
                        <li>Total menús creados.</li>
                        <li>Total pedidos recibidos.</li>
                        <li>Total vendido por día.</li>
                        <li>Menú más vendido.</li>
                        <li>Cierre de sesión.</li>
                    </ol>

                    <h3 id="cargar-menus" style="color:#2a2730;"><strong>Cargar Menús</strong></h3>

                    <p>Para crear un nuevo menú dirijase al módulo de administrador, en la tabla de menús vaya a "Crear Nuevo Menú", se desplegará un formulario donde podrá incluir los detalles del menú. Nombre del menú, descripción, precio, deje desmarcado el campo adición para crear como principal y marque publicado si desea que su nuevo menú esté disponible para sus clientes de inmediato. Guarde el menú y reciba la confirmación.</p>

                    <p class="alert-help">Felicidades, ha creado un nuevo menú<br/> Recuerde que puede crear tantos como quiera y solo marcar como publicados los que desee que su cliente tenga en la selección.</p>

                    <h3 id="cargar-adiciones" style="color:#2a2730;"><strong>Cargar Adiciones</strong></h3>

                    <p>Recuerde que puede agregar también adiciones a sus platos que sus clientes podrán seleccionar por separado. Para ello siga los mismos pasos para crear un nuevo menú y marque la opción "Adición".</p>
                    <p class="alert-help">Puede crear tantas adiciones como quiera y no olvide marcar como publicado aquellas que desee que estén disponibles para sus clientes.</p>

                    <h3 id="ver-pdf-menus" style="color:#2a2730;"><strong>Ver PDF Menús</strong></h3>

                    <img src="/doc/images/crear-pdf-menus.jpg" alt="">

                    <p>Ver, descargar e imprimir un documento con sus menús creados es muy fácil. Basta solo con hacer clic en <strong>"Ver PDF"</strong> y automáticamente se generará el documento con toda la información de sus menús de manera organizada y personalizada.</p>

                    <h3 id="modificar-menus" style="color:#2a2730;"><strong>Modificar Menús</strong></h3>

                    <p>En el momento en que lo desee, puede editar cualquiera de sus creaciones. Basta con hacer clic en el icono de editar <img src="/doc/images/edit.png" alt=""> y podrá sobreescribir los campos de sus menús. <strong>No olvide guardar los cambios</strong> luego de hacerlo y generar de nuevo el pdf de ser necesario.</p>

                    <h3 id="eliminar-menus" style="color:#2a2730;"><strong>Eliminar Menús</strong></h3>

                    <p>Si ya no desea que algún menú se muestre en su tabla de menús, puede eliminarlo haciendo clic en el icono eliminar <img src="//doc/images/delete.png" alt="">, confirme que desea eliminar este menú y dejará de aparecer en su tabla, aunque <strong>aún puede consultarlo en el histórico de menús</strong>.</p>

                    <h3 id="cambiar-estado-menus" style="color:#2a2730;"><strong>Cambiar estado de menús</strong></h3>

                    <p>Si no desea que algún menú o adición se muestren al cliente final, puede deshabilitarlo haciendo clic en editar <img src="//doc/images/edit.png" alt=""> y desmarcando la opción de publicado. El menú seguirá apareciendo en su tabla de menús, pero no aparecerá en la vista de cliente.</p>

                    <h3 id="historico-menus" style="color:#2a2730;"><strong>Histórico de menús</strong></h3>

                    <p><span class="menus-facil">Menús Fácil</span> guarda automáticamente sus menús y adiciones creados durante un tiempo determinado. Puede consultar sus creaciones por días, semanas o incluso meses.</p>

                    <p class="alert-help">Puede no solo consultar sus menús anteriores, sino restaurarlos a su tabla de menús para que aparezcan de nuevo disponibles para sus clientes.</p>

                    <h3 id="pedidos-de-clientes" style="color:#2a2730;"><strong>Pedidos de clientes</strong></h3>

                    <img src="/doc/images/seccion-ultimas-ordenes.jpg" alt="">
                    <p>En esta sección usted verá las ordenes que vayan haciendo sus clientes en tiempo real. <strong>Se mostrará solo el nombre del cliente y el menú seleccionado</strong>. Al generar el documento completo podrá visualizar todos los detalles de la orden.</p>

                    <h3 id="imprimir-pedidos" style="color:#2a2730;"><strong>Imprimir Pedidos</strong></h3>

                    <img src="/doc/images/pedidos-pdf.jpg" alt="">
                    <p>Cuando esté listo, puede generar un pdf imprimible o descargable con las órdenes de sus clientes. <strong>Allí se mostrarán todos los detalles del pedido</strong>: nombre del cliente, teléfono, dirección, selección de menú y si ha hecho alguna observación acerca de la entrega de su pedido.</p>

                    <p class="alert-help">Puede generar tantos pdf como desee, ajustando sus horarios de recepción de órdenes.</p>

                    <h3 id="ver-estadisticas" style="color:#2a2730;"><strong>Ver Estadísticas</strong></h3>

                    <img src="/doc/images/estadisticas.jpg" alt="">
                    <p><span class="menus-facil">Menús Fácil</span> le proporciona cuatro secciones de estadísticas muy útiles para su negocio. Cantidad de menús creados, el número de órdenes hechas en el día, el valor facturado por pedidos en el día y el menú más solicitado en el día. Estos datos le serán de utilidad a la hora de hacer sus balances diarios y medir la efectividad de su aplicación Menús Fácil.</p>

                    <h2 class="dotted" id="clientes"><i>Clientes</i></h2>

                    <p><span class="menus-facil">Menús Fácil - Manager de Pedidos y Menús</span>, cuenta con un <strong>Módulo Clientes</strong> disponible desde cualquier dispositivo con conexión a Internet.</p>

                    <h3 id="vista-clientes" style="color:#2a2730;"><strong>Vista Cliente</strong></h3>

                    <p>Sus clientes ingresan al aplicativo desde el navegador del dispositivo móvil o computadora, a la <strong><i>url personalizada</i></strong> con el nombre de su negocio.</p>

                    <img src="/doc/images/modulo-usuario.jpg" alt="">

                    <ol class="clear">
                        <li>Ficha móvil con información de los menús y adiciones disponibles.</li>
                        <li>Botón para seleccionar menú actual.</li>
                        <li>Resumen de menús seleccionados.</li>
                        <li>Campos para ingresar datos del cliente.</li>
                    </ol>

                    <h3 id="visualizar-menus" style="color:#2a2730;"><strong>Visualizar Menús</strong></h3>

                    <p>En esta sección sus clientes visualizan toda la información de los menús y adiciones disponibles. Nombre del menú, descripción y precio.</p>

                    <h3 id="agregar-pedido" style="color:#2a2730;"><strong>Agregar Pedido</strong></h3>

                    <p>Una vez sus clientes conocen los menús disponibles, pasan a agregar su orden. En la sección de izquierda selecciona el menú deseado y en la sección de la derecha selecciona una o varias adiciones disponibles.</p>

                    <h3 id="confirmar-pedido" style="color:#2a2730;"><strong>Confirmar Pedido</strong></h3>

                    <p>La orden de su cliente será completada cuando ingrese los datos solicitados y pulse el botón <strong>"ENVIAR PEDIDO".</strong></p>


