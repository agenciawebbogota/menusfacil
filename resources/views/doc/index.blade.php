                    <h2 class="dotted" id="vision-general"><i>Visión General</i></h2>

                    <p><span class="menus-facil">Menús Fácil - Manager de Pedidos y Menús</span>, es una versátil aplicación desarrollada para solucionar los problemas de comunicación, gestión y despacho de pedidos de alimentos. Pensada para optimizar los procesos y ahorrar tiempo y dinero; de fácil administración, intuitiva y sencilla para el cliente final.</p>
                    <p class="alert-help">Confiamos en que por medio del uso de esta gran herramienta, sus procesos de despacho y atencíon de clientes sean simplificados</p>

